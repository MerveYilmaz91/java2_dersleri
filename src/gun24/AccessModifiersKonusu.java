package gun24;

public class AccessModifiersKonusu {
    public static void main(String[] args) {
        /**
         * Access Modifiers:
         * 1-public : ile bir değişkeni, sınıfı yada metodu tanımlarsam projenin her yerinden bunlara ulaşabilirim.
         *
         * 2-private : ile bir değişkeni, sınıfı yada metodu tanımlarsam sadece aynı class içerisinden ulaşabiliriz.
         *
         * 3-protected : ile aynı paket içinden erişilebilir. farklı paketler içerisinden erişmek için kalıtım yolu ile
         * miras bırakmış olması gerekir.
         *
         * 4- default : her hangi bir modifier yazmazsak bunu java default olarak algılar.
         * default olanlara sadece aynı paket içerisinden ulaşılabilir.
         */
    }
}
